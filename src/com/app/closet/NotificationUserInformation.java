package com.app.closet;

public class NotificationUserInformation {
	private String	_requestUserUrl, _requestUserId, _requestUserMessage;

	public String getRequestUserUrl() {
		return _requestUserUrl;
	}

	public void setRequestUserUrl(String requestUserUrl) {
		this._requestUserUrl = requestUserUrl;
	}

	public String getRequestUserId() {
		return _requestUserId;
	}

	public void setRequestUserId(String requestUserId) {
		this._requestUserId = requestUserId;
	}

	public String getRequestUserName() {
		return _requestUserMessage;
	}

	public void setRequestUserName(String requestUserName) {
		this._requestUserMessage = requestUserName;
	}
	
}
